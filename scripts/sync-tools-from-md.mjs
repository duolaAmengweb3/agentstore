#!/usr/bin/env node
/**
 * 反向同步:从 content/tools/*.md 生成 src/lib/generated/tools.ts
 *
 * 用法:   npm run sync:tools
 *
 * 这个脚本的角色:
 *  - content/tools/*.md 是 **真源**(人 PR 贡献入口,有 git history)
 *  - 运行它把 md 转成 TypeScript 数组写入 src/lib/generated/tools.ts
 *  - 那个 .ts 文件是 client-safe 的,能直接 import 到 React client 组件
 *  - CI 在 build 前跑这个脚本,保证产物最新
 *
 * 为什么不让前端运行时读 md:
 *  - node:fs 不能进 client bundle(React Server/Client 边界)
 *  - 这样的"md -> ts 编译产物"模式更符合 Next.js 生态
 */

import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import matter from 'gray-matter';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const INPUT_DIR = path.join(ROOT, 'content/tools');
const OUTPUT_FILE = path.join(ROOT, 'src/lib/generated/tools.ts');

async function main() {
  const files = (await fs.readdir(INPUT_DIR)).filter((f) => f.endsWith('.md'));
  console.log(`Reading ${files.length} markdown files from ${path.relative(ROOT, INPUT_DIR)}/`);

  const tools = [];
  for (const filename of files) {
    const raw = await fs.readFile(path.join(INPUT_DIR, filename), 'utf8');
    const { data } = matter(raw);
    const entry = {
      slug: String(data.slug || filename.replace(/\.md$/, '')),
      name: String(data.name || data.slug),
      author: String(data.author || 'unknown'),
      tagline: {
        en: String(data.tagline_en || ''),
        zh: String(data.tagline_zh || data.tagline_en || ''),
      },
      category: data.category || 'data',
      icon: String(data.icon || '📦'),
      official: !!data.official,
      metrics: {
        ...(data.metrics?.smitheryCalls != null && { smitheryCalls: data.metrics.smitheryCalls }),
        ...(data.metrics?.npmMonthly != null && { npmMonthly: data.metrics.npmMonthly }),
        ...(data.metrics?.githubStars != null && { githubStars: data.metrics.githubStars }),
        ...(data.metrics?.weeklyGrowthPct != null && { weeklyGrowthPct: data.metrics.weeklyGrowthPct }),
      },
      score: typeof data.score === 'number' ? data.score : 7.0,
    };
    if (data.summary_en || data.summary_zh) {
      entry.summary = {
        en: String(data.summary_en || data.summary_zh || ''),
        zh: String(data.summary_zh || data.summary_en || ''),
      };
    }
    tools.push(entry);
  }

  // 按 slug 去重 + 排序(稳定 diff)
  const byslug = new Map(tools.map((t) => [t.slug, t]));
  const sorted = [...byslug.values()].sort((a, b) => a.slug.localeCompare(b.slug));

  const out = `// ⚠️ AUTO-GENERATED by scripts/sync-tools-from-md.mjs
// DO NOT EDIT MANUALLY. Edit files in content/tools/*.md instead.
// Run: npm run sync:tools

import type { Tool } from '../mock-data';

export const toolsFromMarkdown: Tool[] = ${JSON.stringify(sorted, null, 2)};
`;

  await fs.mkdir(path.dirname(OUTPUT_FILE), { recursive: true });
  await fs.writeFile(OUTPUT_FILE, out, 'utf8');

  console.log(`Wrote ${sorted.length} tools to ${path.relative(ROOT, OUTPUT_FILE)}`);
  console.log('Tip: import this from mock-data.ts if you want md as the runtime source.');
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
